CREATE TABLE Income_Statement (
  ID INT NOT NULL AUTO_INCREMENT,
  cik INT NOT NULL,
  calendarYear VARCHAR(5) NOT NULL,
  period VARCHAR(5) NOT NULL,
  revenue DECIMAL(24,4),
  costOfRevenue DECIMAL(24,4),
  grossProfit DECIMAL(24,4),
  researchAndDevelopmentExpenses DECIMAL(24,4),
  generalAndAdministrativeExpenses DECIMAL(24,4),
  sellingAndMarketingExpenses DECIMAL(24,4),
  sellingGeneralAndAdministrativeExpenses DECIMAL(24,4),
  otherExpenses DECIMAL(24,4),
  operatingExpenses DECIMAL(24,4),
  costAndExpenses DECIMAL(24,4),
  interestIncome DECIMAL(24,4),
  interestExpense DECIMAL(24,4),
  depreciationAndAmortization DECIMAL(24,4),
  ebitda DECIMAL(24,4),
  operatingIncome DECIMAL(24,4),
  totalOtherIncomeExpensesNet DECIMAL(24,4),
  incomeBeforeTax DECIMAL(24,4),
  incomeTaxExpense DECIMAL(24,4),
  netIncome DECIMAL(24,4),
  KEY(ID),
  PRIMARY KEY (cik, calendarYear, period)
);